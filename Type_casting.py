# Type Conversion
# Type Conversion is nothing but the compiler converts the data type automatically
# Here 'a' is in int and 'b' is in float the compiler coverts a into float
# int is converted to float becoz float is of higher order than int
a=3
b=2.5
print(a+b)

# Type Casting
# Type Casting is that the programmer converts one datatype to another datatype
a=3
b=2.5
a1=float(a)
print(a1)
print(a+b)